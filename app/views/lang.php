function _T($str, $param="")
{
  //return $str;
  Global $app;

  $ENG = $app->config->lang;
  $arrENG = getEngArray();

  if (!$ENG) 
  {
	if (!$param)
		return $str;
	else 
		return sprintf($str, $param);
  }

  if (array_key_exists($str, $arrENG))
  {
	if (!$param)
		return $arrENG[$str];
	else 
		return sprintf($arrENG[$str], $param);
  }

  return $str;
}
